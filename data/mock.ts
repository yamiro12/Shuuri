/**
 * REGLA CRÍTICA DE COMPLIANCE
 * Bloqueo absoluto: si la cert del rubro está vencida o no existe → false
 */
export function tecnicoPuedeAtenderRubro(tecnico: Tecnico, rubro: Rubro): boolean {
  if (tecnico.bloqueado) return false;
  if ((tecnico.bloqueadoRubros as string[]).includes(rubro as string)) return false;
  const certStatus = (tecnico.certPorRubro as Record<string, string | undefined>)[rubro as string];
  if (!certStatus || certStatus === 'vencida') return false;
  return true;
}

export function getTecnicosDisponiblesParaRubro(rubro: Rubro): Tecnico[] {
  return TECNICOS.filter(t => tecnicoPuedeAtenderRubro(t, rubro));
}

